import math

print("##########################################")
print("Algoritmo calcula el area de un circulo")
print("------------------------------------------")
radio = int(input("Por favor digite el radio del circulo al cual le quiere calcuar el area: "))

area = math.pi * (radio*radio)

print ("El area del circulo es: " + str(area))


